CREATE DATABASE IF NOT EXISTS whadaadmin DEFAULT CHARACTER SET 'utf8';

CREATE TABLE IF NOT EXISTS whadaadmin.settings (
  id     INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
  name   VARCHAR(64) NOT NULL UNIQUE,
  data   TEXT
) ENGINE=InnoDB;

CREATE TABLE IF NOT EXISTS whadaadmin.privs (
  id     INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
  name   VARCHAR(64) NOT NULL UNIQUE,
  data   TEXT
) ENGINE=InnoDB;

CREATE TABLE IF NOT EXISTS whadaadmin.users (
  id     INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
  name   VARCHAR(64) NOT NULL UNIQUE,
  data   TEXT
) ENGINE=InnoDB;

CREATE DATABASE IF NOT EXISTS whadasession DEFAULT CHARACTER SET 'utf8';

CREATE TABLE IF NOT EXISTS whadasession.session (
  sid          VARCHAR(32) UNIQUE PRIMARY KEY,
  data         TEXT,
  expires      INTEGER UNSIGNED NOT NULL
) ENGINE=InnoDB;
